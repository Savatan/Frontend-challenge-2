import type { Post } from "@/shared/api/jsonPlaceholderApi";

export function translatePostToRussian(post: Post): Post {
  if (post.id === 1) {
    return {
      ...post,
      title:
        "следует ли совершать возмездие, обеспечивать ответственность, кроме права выбора",
      body:
        "потому что есть обязанность и ответственность; ответственность, отвергающая последствия, быстрая и совместная; ответственность за страдания такова, что всё полностью наше дело, но при этом есть вещи, которые возникают неожиданно, как архитектурный замысел",
    };
  }

  if (post.id === 2) {
    return {
      ...post,
      title: "кто это такой",
      body:
        "это природа вещей и времени жизни; за ними следует то, что не содержит в себе вины, но приносит боль и блаженство; есть страдания, от которых не скрыться, и приятные вещи, которые всё же приходится отвергать; открываются такие обстоятельства, с которыми нельзя не считаться",
    };
  }

  return {
    ...post,
    title: `Пост №${post.id}`,
    body: `Текст поста №${post.id}`,
  };
}
